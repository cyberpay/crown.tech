                    <h2 style="margin-top:50px">Trons on the Crown network serve to:</h2>
                    
                    <h4>Secure and stabilize the network and transactions</h4>
                    <p>For those interested in how this works, there are ways that network security can be accomplished using mathematical properties which don’t involve mining algorithms, but functionally leverage randomness and network topology.  These techniques are used by a variety of different systems, from WeChat to Zookeeper -- just look up the Paxos Algorithm and go from there.  The Dash paxos implementation with InstantX is an elegant example.  
                    The network is secured by both the Trons and merge-mining.  We like having multiple layers of security.</p>
                    

                           
                    <h4>Provide another layer of governance</h4>
                    <p>Trons will be able to make and vote on proposals for allocating the 10% of the block rewards which has been held back since the last code update in February 2017.  None of the funds held back from the block rewards have been allocated yet, and the existing development fund is composed of tokens contributed personally by members of the core team so that the project would have the resources to get to the point at which Tron voting would be turned on.  This code is in place and it is just a matter of turning it on this function that the platform inherits from Dash in the next update.</p>

                    <h4>Provide Application Developers and Tron Operators with Business Opportunities</h4>
                    <p>Once the API is developed, developers will be able to integrate Crown API calls and package applications which Tron operators would be able to offer to users managing the provisioning of compute resources, and payment for the application, through the Crown API in concert with other API’s or development tools.  Trons are, in a sense, franchises to operate a cloud application business running open source applications and accepting payment denominated in CRW.</p>
                    
                    <p>The overall goal of the project is to do something useful with crypto.  The first use is to see if we can create an application economy running a cloud of open source applications.  The Trons are the backbone to the Crown Cloud, which doesn’t exist yet – but which we hope to work with the community to build.</p>
                   
                    <h2>Why Tron?</h2>
                    <p>
                        Well… we needed a name.  Tron is close to the old name for the servers, throne – but a “tron” is active and not passive, and “tron” refers to the history of crypto where Hal Finney was involved with building the Atari game The Adventure of Tron.  
                    </p>
                    <p>
                        The key idea is that Trons are active server nodes on the network.  A Tron can generate revenues by confirming transactions on the network – but it will also be able to use the Crown API, alongside other API’s to allow developers to build other businesses as well.
                    </p>
