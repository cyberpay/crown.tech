						Inspired by the Dash system, our growing community is enabled to use the Crown governance system 
						proposals to decide what features will be implemented on the blockchain. The proposal system allows 
						Tron Masternodes to vote for or against proposals, which can then be implemented by the Crown developers. 
					</p>
                    <p>
                        <i>A very good example is early in 2016, when Dash's Core Team submitted a proposal to the network 
						asking whether the blocksize should be increased to 2 MB. Within 24 hours, consensus had been 
						reached to approve this change. Compare this to Bitcoin, where debate on the blocksize has been 
						raging for nearly three years.</i>
                    </p>
                    <p>
                        The Crown proposal system also provides a means for Crown to fund its own development. While other 
						projects have to depend on donations or premined endowments, Crown uses 10% of the block reward to 
						fund its own development. Every time a block is mined, 45% of the reward goes to the miner, 45% goes 
						to a Tron Masternode, and the remaining 10% is not created until the end of the month. During the month, 
						anybody can make a budget proposal to the network. If that proposal is approved by at least 10% of the 
						Tron Masternode network, then at the end of the month a series of "superblocks" will be created. At that 
						time, the block rewards that were not paid out (10% of each block) will be used to fund approved proposals. 
						The network thus funds itself by reserving 10% of the block reward for budget projects.
                    </p>
					
